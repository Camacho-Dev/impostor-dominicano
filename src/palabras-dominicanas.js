// Función para obtener una palabra aleatoria de categorías seleccionadas
export function obtenerPalabraAleatoria(categorias) {
    // Si categorias es un string, convertir a array
    if (typeof categorias === 'string') {
        if (categorias === "mezclado" || categorias === "todas") {
            categorias = Object.keys(palabrasDominicanas);
        } else {
            categorias = [categorias];
        }
    }
    
    // Obtener todas las palabras de las categorías seleccionadas
    const todasLasPalabras = categorias.flatMap(cat => 
        palabrasDominicanas[cat] || []
    );
    
    if (todasLasPalabras.length === 0) {
        return "Dominicano";
    }
    
    // Mezclar aleatoriamente múltiples veces para mayor aleatoriedad
    let palabrasMezcladas = [...todasLasPalabras];
    for (let i = 0; i < 5; i++) {
        palabrasMezcladas = palabrasMezcladas.sort(() => Math.random() - 0.5);
    }
    
    // Usar múltiples fuentes de aleatoriedad
    const random1 = Math.random();
    const random2 = Math.random();
    const random3 = Math.random();
    const indice = Math.floor((random1 + random2 + random3) / 3 * palabrasMezcladas.length);
    
    return palabrasMezcladas[indice];
}

